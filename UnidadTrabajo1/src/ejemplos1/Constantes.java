package ejemplos1;

public class Constantes {

	public static void main(String[] args) {
		
		//Variable
				int num=22;
				
				//Constante
				final double PI=3.141592; //<<--- Se antepone "final" a las contantes antes de declararlas, utilizamos "double" a las constantes con decimales
				
				//Operaciones
				num=num+1;
				
				System.out.println(num);
				System.out.println(PI);
	}

}
