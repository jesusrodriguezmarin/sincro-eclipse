package ejemplos3;

public class Concatenacion {

	public static void main(String[] args) {

		// Declaración e inicialización de variables
		int a=5, b=19;
		
		// Mostramos distintas operaciones por pantalla
		System.out.println(a+b);						// 15
		System.out.println("Suma: "+a+b);				// Suma: 510
		System.out.println("Suma: "+(a+b));				// Suma: 15
		System.out.println(a+b+" resultado final");		// 15 resultado final

	}

}
