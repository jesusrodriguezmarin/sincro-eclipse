package hojaEjercícios;

public class Ej11Longitud {

	public static void main(String[] args) {

		// Declaración e inicialización
		int radio=3;
		final double PI=3.1416;
		double longitud;
		
		// Algoritmo operación lomgitud
		longitud=2*PI*radio;
		
		// Resultado impreso por pantalla
		System.out.println("La longitud de una circuferencia con radio: "+radio+" es: "+longitud+" metros ");
		
	}

}
