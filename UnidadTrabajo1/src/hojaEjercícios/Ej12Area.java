package hojaEjercícios;

public class Ej12Area {

	public static void main(String[] args) {
		
		// Declaración e inicialización
		double radio=3.6;
		final double PI=3.1416;
		double area;
		
		// Operación para calcular el area
		area=PI*radio*radio;
		
		// Resultado impreso en pantalla
		System.out.println("El área de un círculo con radio: "+radio+" es: "+area+" metros cuadrados ");

		
		
	}

}
