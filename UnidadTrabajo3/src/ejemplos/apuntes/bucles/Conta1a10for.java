package ejemplos.apuntes.bucles;

public class Conta1a10for {

	public static void main(String[] args) {
		// Contador de 1 a 10 con bucle for
		System.out.println("Contador de 1 a 10: ");
		for (int conta = 1; conta <= 10; conta++)
			System.out.print(conta+" ");
			
		// 	Contador de 10 a 1 con bucle for
		System.out.println("\nContador de 10 a 1");
		for (int conta = 10; conta >= 1; conta--)
			System.out.print(conta+" ");
	}

}
