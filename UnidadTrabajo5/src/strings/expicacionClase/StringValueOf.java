package strings.expicacionClase;

public class StringValueOf {

	public static void main(String[] args) {

		// Cambio de int a cadena
		int num = 5;
		String cad = String.valueOf(num);
		
		// Opera
		System.out.println(num+10);
		// Concatena
		System.out.println(cad+10);
		
		// Cambio de float a cadena
		float num2=5;
		String cad2=String.valueOf(num2);
		// Opera
		System.out.println(num2+10);
		// Concatena
		System.out.println(cad2+10);

	}

}
